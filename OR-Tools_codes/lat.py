class lat_to_sbox():
    def __init__(self, n, workers, LAT):
        self.n = n
        self.workers = workers
        self.LAT = LAT
        self.N = int(math.log(self.n, 2))
        self.model = cp_model.CpModel()
        self.solver = cp_model.CpSolver()


    def get_bin(self, x):
        B = []
        for i in range(self.N):
            B.append(( x >> i ) & 1)
        return B

    def generate_model(self):

        Y = [self.model.NewIntVar(0, self.n - 1, f'Y[{i}]') for i in range(self.n)]
        A = {}

        for i in range(self.n):
            for j in range(self.n):
                A[i, j] = self.model.NewBoolVar(f'A[{i},{j}]')

        X = {}
        for i in range(self.n):
            for j in range(self.n):
                X[i, j] = self.model.NewBoolVar(f'X[{i},{j}]')

        for j in range(self.n):
            self.model.Add(sum(X[i, j] for i in range(self.n)) == 1)

        for i in range(self.n):
            self.model.Add(sum(X[i, j] for j in range(self.n)) == 1)

        for i in range(self.n):
            self.model.Add(sum(j*X[i, j] for j in range(self.n)) == Y[i])

        for i in range(self.n):
            self.model.Add(Y[i] == sum((1 << j) * A[i, j] for j in range(self.N)))

        S = {}
        V = {}
        M = {}
        for la in range(self.n - 1):
            for lb in range(self.n):
                for i in range(self.n):
                    S[la, lb, i] = self.model.NewBoolVar(f'S[{la},{lb},{i}]')
                    M[la, lb, i] = self.model.NewBoolVar(f'M[{la},{lb},{i}]')
                    V[la, lb, i] = self.model.NewIntVar(0, math.ceil((self.N+1)/2), f'V[{la},{lb},{i}]')


        T1 = [0]*self.N
        for la in range(self.n-1):
            LA = self.get_bin(la + 1)
            for lb in range(self.n):
                LB = self.get_bin(lb)

                for x in range(self.n):
                    LX = self.get_bin(x)
                    bit = 0
                    for k in range(self.N):
                        bit ^= (LA[k] & LX[k])
                    for k in range(self.N):
                        T1[k] = A[x, k]

                    self.model.Add(M[la,lb, x] == 1 - S[la, lb, x])
                    temp = 0
                    for k in range(self.N):
                        temp += LB[k]* T1[k]
                    temp += bit
                    temp += M[la, lb, x]
                    self.model.Add(temp == 2 * V[la, lb, x])

                self.model.Add(sum(S[la, lb, i] for i in range(self.n)) == int(self.LAT[la+1][lb] + self.n/2))


        return Y

    def solve_model(self):
        Y = self.generate_model()
        self.solver.parameters.num_search_workers = self.workers
        # self.solver.parameters.max_time_in_seconds = 10.0
        # self.solver.parameters.log_search_progress = True

        status = self.solver.Solve(self.model)
        if status == cp_model.FEASIBLE or status == cp_model.OPTIMAL:
            Z = [self.solver.Value(Y[i]) for i in range(self.n)]
            print(f'S = SBox({Z})')
            print(f'Time = {self.solver.WallTime()} s')
        else:
            print('No solution found.')
            print(f'Time = {self.solver.WallTime()} s')


