class ddt_to_sbox():
    def __init__(self, n, workers, DDT, rows):
        self.n = n
        self.DDT = DDT
        self.workers = workers
        self.rows = rows
        self.model = cp_model.CpModel()
        self.solver = cp_model.CpSolver()

    def solutions_pairs(self):
        INDEXS = []
        for alpha in range(1, self.n):
            T = []
            for i in range(self.n):
                for j in range(i + 1, self.n):
                    if i ^ j == alpha:
                        T.append([i, j])
            INDEXS.append(T)
        return INDEXS

    def generate_model(self):
        N = int(math.log(self.n, 2))
        INDEXS = self.solutions_pairs()
        Y = [self.model.NewIntVar(0, self.n - 1, f'Y[{i}]') for i in range(self.n)]
        A = {}
        C = {}

        for i in range(self.n):
            for j in range(self.n):
                A[i, j] = self.model.NewBoolVar(f'A[{i},{j}]')

        for i in range(self.n - 1):
            for j in range(int(self.n / 2)):
                for k in range(N):
                    C[i, j, k] = self.model.NewBoolVar(f'C[{i},{j},{k}]')


        X = {}
        for i in range(self.n):
            for j in range(self.n):
                X[i, j] = self.model.NewBoolVar(f'X[{i},{j}]')

        for j in range(self.n):
            self.model.Add(sum(X[i, j] for i in range(self.n)) == 1)

        for i in range(self.n):
            self.model.Add(sum(X[i, j] for j in range(self.n)) == 1)

        for i in range(self.n):
            self.model.Add(sum(j * X[i, j] for j in range(self.n)) == Y[i])

        for i in range(self.n):
            self.model.Add(Y[i] == sum((1 << j) * A[i, j] for j in range(N)))

        for i in range(self.rows - 1):
            for j in range(int(self.n / 2)):
                T1 = [0] * N
                T2 = [0] * N
                T3 = [0] * N
                for k in range(N):
                    T1[k] = A[INDEXS[i][j][0], k]
                    T2[k] = A[INDEXS[i][j][1], k]
                    T3[k] = C[i, j, k]
                D = {}
                for k in range(N):
                    D[k] = self.model.NewBoolVar(f'D[{i},{j},{k}]')
                for k in range(N):
                    self.model.Add(T1[k] + T2[k] + T3[k] == 2 * D[k])

        for row in range(self.rows - 1):
            S = {}
            for i in range(int(self.n / 2)):
                for j in range(self.n - 1):
                    S[i, j] = self.model.NewBoolVar(f'S[{row},{i},{j}]')

            for j in range(self.n - 1):
                self.model.Add(sum(S[i, j] for i in range(int(self.n / 2))) == int(self.DDT[row + 1][j + 1] / 2))

            for i in range(int(self.n / 2)):
                self.model.AddExactlyOne(S[i, j] for j in range(self.n - 1))

            for i in range(int(self.n / 2)):
                self.model.Add(sum((j + 1) * S[i, j] for j in range(self.n - 1)) \
                               == sum((1 << k) * C[row, i, k] for k in range(N)))

        return Y

    def solve_model(self):
        Y = self.generate_model()
        self.solver.parameters.num_search_workers = self.workers
        # self.solver.parameters.max_time_in_seconds = 10.0
        # self.solver.parameters.log_search_progress = True

        status = self.solver.Solve(self.model)

        if status == cp_model.FEASIBLE or status == cp_model.OPTIMAL:
            Z = [self.solver.Value(Y[i]) for i in range(self.n)]
            print(f'S = SBox({Z})')
            print(f'Time = {self.solver.WallTime()} s')
        else:
            print('No solution found.')
            print(f'Time = {self.solver.WallTime()} s')


